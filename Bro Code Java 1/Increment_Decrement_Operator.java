public class Increment_Decrement_Operator {
    public static void main(String[] args) {
        // Incremeent and Decrement operators

        // INCREMENT
        int x = 1;
        // Instead of using x = x + 1;
        // we use
        x++;

        // DECREMENT
        /*
         * int x = 1;
         * Instead of using x = x - 1;
         * we use x--;
         */
        System.out.println(x);
    }
}